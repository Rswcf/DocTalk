def summarize(items: list[dict[str, Any]]) -> dict[str, int]:
    out: dict[str, int] = {}
    for item in items:
        out[item["status"]] = out.get(item["status"], 0) + 1
    return out


def main() -> int:
    args = parse_args()
    inventory = load_json(args.inventory)
    concrete = [route_surface(item) for item in inventory["concrete"]]
    dynamic = [dynamic_surface(item) for item in inventory["dynamicTemplates"]]
    api = [api_surface(item) for item in inventory["apiRoutes"]]
    axes = build_objective_axes()

    missing_evidence = []
    for section_name, section in [("concrete_routes", concrete), ("dynamic_templates", dynamic), ("api_routes", api), ("objective_axes", axes)]:
        for item in section:
            for ref in item["evidence"]:
                if not ref["exists"]:
                    missing_evidence.append({
                        "section": section_name,
                        "surface": item.get("route") or item.get("key"),
                        "path": ref["path"],
                    })

    report = {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "inventory": args.inventory,
        "complete": False,
        "completion_reason": "Coverage evidence exists for discovered surfaces, but objective axes still have provider, deployment, OAuth/email, and safe-production-payment blockers.",
        "summary": {
            "concrete_routes": len(concrete),
            "dynamic_templates": len(dynamic),
            "api_routes": len(api),
            "objective_axes": len(axes),
            "concrete_route_statuses": summarize(concrete),
            "dynamic_template_statuses": summarize(dynamic),
            "api_route_statuses": summarize(api),
            "objective_axis_statuses": summarize(axes),
            "uncovered_concrete_routes": len([item for item in concrete if item["status"] == "uncovered"]),
            "uncovered_dynamic_templates": len([item for item in dynamic if item["status"] == "uncovered"]),
            "uncovered_api_routes": len([item for item in api if item["status"] == "uncovered"]),
            "missing_evidence_artifacts": len(missing_evidence),
        },
        "missing_evidence": missing_evidence,
        "concrete_routes": concrete,
        "dynamic_templates": dynamic,
        "api_routes": api,
        "objective_axes": axes,
    }

    out = ROOT / args.json_out
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(json.dumps(report, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
    print(
        "SURFACE_COVERAGE_AUDIT complete={complete} routes={routes} dynamic={dynamic} api={api} axes={axes} missing_evidence={missing}".format(
            complete=str(report["complete"]).lower(),
            routes=len(concrete),
            dynamic=len(dynamic),
            api=len(api),
            axes=len(axes),
            missing=len(missing_evidence),
        )
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
