from typing import Any

ROOT = Path(__file__).resolve().parents[2]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--inventory",
        default=".collab/tasks/qa-route-inventory-2026-05-10.json",
        help="Route inventory JSON generated by qa_route_inventory.js.",
    )
    parser.add_argument("--json-out", required=True)
    return parser.parse_args()


def load_json(relative: str) -> dict[str, Any]:
    path = ROOT / relative
    return json.loads(path.read_text(encoding="utf-8"))


def file_status(relative: str) -> str | None:
    path = ROOT / relative
    if not path.exists() or path.suffix != ".json":
        return None
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
    except Exception as exc:
        return f"load_error:{type(exc).__name__}"
    return str(data.get("status") or data.get("result") or data.get("mode") or "")


def artifact(relative: str) -> dict[str, Any]:
    path = ROOT / relative
    return {
        "path": relative,
        "exists": path.exists(),
        "status": file_status(relative),
    }


