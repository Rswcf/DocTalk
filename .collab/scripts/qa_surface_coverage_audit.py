"""Map DocTalk product surfaces to concrete QA evidence.

This audit does not run product tests. It checks that the current long-running
QA corpus has explicit evidence for each discovered route/API surface and each
objective axis, while preserving open blockers instead of turning coverage into
a false completion signal.
"""

from __future__ import annotations

import argparse
import json
from datetime import datetime, timezone
