PUBLIC_KINDS = {"public", "seo-public", "seo-content"}


def evidence_refs(keys: list[str]) -> list[dict[str, Any]]:
    refs: list[dict[str, Any]] = []
    seen: set[str] = set()
    for key in keys:
        for item in EVIDENCE[key]:
            if item not in seen:
                refs.append(artifact(item))
                seen.add(item)
    return refs


def area_key(area: str) -> str | None:
    if area in {"admin", "billing", "collections", "document-diff", "profile"}:
        return area
    if area == "reader":
        return "reader"
    if area == "sharing":
        return "sharing"
    return None


def route_surface(route: dict[str, Any]) -> dict[str, Any]:
    keys = ["route_inventory"]
    risks: list[str] = []
    status = "covered"
    kind = route["kind"]
    area = route["area"]

    if kind in PUBLIC_KINDS:
        keys.extend(["public_page_regression", "public_static_metadata_links_a11y_perf"])
        status = "covered_with_open_production_drift"
        risks.append("Current production public regression still has deploy-drift failures/warnings; local fixes require deploy/retest.")
        if area == "demo":
            keys.extend(["rag_pdf", "reader"])
        if area == "contact":
            keys.append("contact_form")
        if area == "tools":
            keys.append("tools_ux")
        if route["route"] in {"/", "/privacy", "/use-cases/healthcare"} or route["route"].startswith("/blog/"):
            risks.append("This route family is in the current production drift set for CSP, fragments, links, or heading-order advisories.")
    elif kind == "auth":
        keys.append("auth_pages")
    elif kind == "gated":
        key = area_key(area)
        if key:
            keys.append(key)
            status = "covered_local_fixture"
        else:
            status = "uncovered"
            risks.append("No area-specific evidence mapping for gated route.")
    else:
        status = "needs_review"
        risks.append(f"Unhandled route kind: {kind}")

    return {
        "route": route["route"],
        "template": route.get("template"),
        "kind": kind,
        "area": area,
        "status": status,
        "risks": risks,
        "evidence": evidence_refs(keys),
    }


def dynamic_surface(template: dict[str, Any]) -> dict[str, Any]:
    route = template["route"]
    if route in {"/blog/[slug]", "/blog/category/[category]", "/demo/[sample]"}:
        keys = ["route_inventory", "public_page_regression", "public_static_metadata_links_a11y_perf"]
        if route == "/demo/[sample]":
            keys.extend(["rag_pdf", "reader"])
        status = "expanded_concrete_routes_covered_with_open_production_drift"
        risks = ["This dynamic template is expanded into concrete routes by the inventory; current production public drift still needs deploy/retest."]
    elif route == "/d/[documentId]":
        keys = ["reader", "backend_document_api", "rag_pdf"]
        status = "covered_with_open_production_drift"
        risks = ["Current production demo direct citation highlight fails until local reader fix is deployed/retested."]
    elif route == "/collections/[collectionId]":
        keys = ["collections", "structured_outputs"]
        status = "covered_local_fixture"
        risks = []
    elif route == "/shared/[token]":
        keys = ["sharing"]
        status = "covered_local_fixture"
        risks = []
    else:
        keys = ["route_inventory"]
        status = "uncovered"
        risks = ["No dynamic-template evidence mapping."]
    return {
        "route": route,
        "area": template["area"],
        "requires": template.get("requires"),
        "status": status,
        "risks": risks,
        "evidence": evidence_refs(keys),
    }


def api_surface(route: dict[str, Any]) -> dict[str, Any]:
    api_route = route["route"]
    keys = ["frontend_api_guards"]
    status = "covered"
    risks: list[str] = []
    if api_route == "/api/proxy/[...path]":
        keys.extend(["backend_document_api", "permissions"])
    elif api_route == "/api/auth/[...nextauth]":
        keys.append("auth_pages")
        status = "covered_except_successful_oauth_email_callback"
        risks.append("Provider UI and guards are covered; successful OAuth/email callback remains externally blocked.")
    elif api_route == "/api/upload-token":
        keys.append("permissions")
    return {
        "route": api_route,
        "area": route["area"],
        "requires": route.get("requires"),
        "status": status,
        "risks": risks,
        "evidence": evidence_refs(keys),
    }


def objective_axis(
    key: str,
    label: str,
    status: str,
    evidence_keys: list[str],
    gaps: list[str],
) -> dict[str, Any]:
    return {
        "key": key,
        "label": label,
        "status": status,
        "evidence": evidence_refs(evidence_keys),
        "gaps": gaps,
    }


